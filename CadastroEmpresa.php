        function validarCNPJ(input) {
            const cnpj = input.value.replace(/[^\d]+/g, ''); // Remove caracteres não numéricos
            const erroCnpj = document.getElementById('erroCnpj'); // Elemento para exibir o erro

            // Verifica se o CNPJ tem 14 dígitos
            if (cnpj.length !== 14) {
                erroCnpj.textContent = "CNPJ inválido. Deve conter 14 dígitos.";
                input.focus(); // Coloca o foco no campo de CNPJ
                return false;
            }

            // Verifica se todos os dígitos são iguais, o que tornaria o CNPJ inválido
            if (/^(\d)\1+$/.test(cnpj)) {
                erroCnpj.textContent = "CNPJ inválido. Todos os dígitos são iguais.";
                input.focus(); // Coloca o foco no campo de CNPJ
                return false;
            }

            // Calcula o primeiro dígito verificador
            let soma = 0;
            let peso = 5;
            for (let i = 0; i < 12; i++) {
                soma += Number(cnpj.charAt(i)) * peso;
                peso = (peso === 2) ? 9 : peso - 1;
            }
            let digitoVerificador1 = (11 - (soma % 11)) % 10;

            // Verifica se o primeiro dígito verificador está correto
            if (Number(cnpj.charAt(12)) !== digitoVerificador1) {
                erroCnpj.textContent = "CNPJ inválido.";
                input.focus(); // Coloca o foco no campo de CNPJ
                return false;
            }

            // Calcula o segundo dígito verificador
            soma = 0;
            peso = 6;
            for (let i = 0; i < 13; i++) {
                soma += Number(cnpj.charAt(i)) * peso;
                peso = (peso === 2) ? 9 : peso - 1;
            }
            let digitoVerificador2 = (11 - (soma % 11)) % 10;

            // Verifica se o segundo dígito verificador está correto
            if (Number(cnpj.charAt(13)) !== digitoVerificador2) {
                erroCnpj.textContent = "CNPJ inválido.";
                input.focus(); // Coloca o foco no campo de CNPJ
                return false;
            }

            // Se tudo estiver correto
            erroCnpj.textContent = ""; // Limpa a mensagem de erro
            return true; // Retorna true para indicar que o CNPJ é válido
        }

        // Validação no envio do formulário
        document.querySelector('form').addEventListener('submit', function (event) {
