        function validarTelefone(input) {
            const telefone = input.value; // Pega o valor do input
            const regexTelefone = /^(\d{2})(\d{8,9})$/; // Regex para validar telefone brasileiro
            const erroTelefone = document.getElementById('erroTelefone'); // Elemento para exibir o erro

            // Remove todos os caracteres não numéricos
            const telefoneLimpo = telefone.replace(/\D/g, '');

            // Lista de DDDs válidos no Brasil
            const dddsValidos = [11, 12, 13, 14, 15, 16, 17, 18, 19, 21, 22, 24, 27, 28, 31, 32, 33, 34, 35, 37, 38, 41, 42, 43, 44, 45, 46, 47, 48, 49, 51, 53, 54, 55, 61, 62, 63, 64, 65, 66, 67, 68, 69, 71, 73, 74, 75, 77, 79, 81, 82, 83, 84, 85, 86, 87, 88, 89, 91, 92, 93, 94, 95, 96, 97, 98, 99];

            // Extrai os dois primeiros dígitos (DDD)
            const ddd = telefoneLimpo.substring(0, 2);

            // Verifica se o telefone tem o formato correto
            if (!regexTelefone.test(telefoneLimpo)) {
                erroTelefone.textContent = "Formato de telefone inválido. Use DDD + número (ex: 13996874561).<br>";
                input.focus(); // Coloca o foco no campo de telefone
                return false; // Retorna false para indicar que o telefone é inválido
            }
            // Verifica se o DDD é válido
            else if (!dddsValidos.includes(Number(ddd))) {
                erroTelefone.textContent = "DDD inválido.";
                input.focus(); // Coloca o foco no campo de telefone
                return false; // Retorna false para indicar que o DDD é inválido
            }
            // Se tudo estiver correto
            else {
                erroTelefone.textContent = ""; // Limpa a mensagem de erro
                return true; // Retorna true para indicar que o telefone é válido
            }
        }

